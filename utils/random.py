def random_hsv(iterations=100):
    """
    Generate random colors for iterations
    """
    colors = []
    for it in range(iterations):
        random.seed(it)
        rand_color = random.choices(range(256), k=3)
        hsv_str = ""
        for color in rand_color:
            hsv_str += f"{color / 256} "
        hsv_str = hsv_str[:-1]
        colors.append(hsv_str)
    return colors
